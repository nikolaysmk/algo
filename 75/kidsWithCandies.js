// Имеется n детей с конфетами. Вам дан целочисленный массив candies, где каждая candies[i] представляет собой количество конфет у i-го ребенка, и целое число extraCandies, обозначающее количество дополнительных конфет, которые есть у вас.
// Возвращает булев массив result длины n, где result[i] равен true, если после того, как вы отдадите i-му ребенку все extraCandies, у него будет самое большое количество конфет среди всех детей, или false в противном случае.
// Обратите внимание, что наибольшее количество конфет может быть у нескольких детей.
// Переведено с помощью www.DeepL.com/Translator (бесплатная версия)
// Найти максимальное количество конфет среди всех детей. Это нужно, чтобы понять, будет ли ребенок с дополнительными конфетами иметь максимальное или одно из максимальных количеств конфет.

// Создать массив для хранения результатов. 
// Этот массив будет содержать булевые значения (true или false) для каждого ребенка в зависимости от того, сможет ли он иметь максимальное количество конфет после получения дополнительных.
// Определить, сможет ли каждый ребенок иметь максимальное количество конфет. 
// Для каждого ребенка мы добавляем к его текущему количеству конфет число extraCandies и сравниваем с максимальным количеством конфет. 
// Если после добавления у ребенка будет столько же или больше конфет, чем максимальное количество, мы ставим в массив результатов true, иначе - false.

/**
 * @param {number[]} candies
 * @param {number} extraCandies
 * @return {boolean[]}
 */
var kidsWithCandies = function(candies, extraCandies) {
  let maxCandies = Math.max(...candies); // Находим максимальное количество конфет
  let result = [];

  for (let i = 0; i < candies.length; i++) {
    result.push(candies[i] + extraCandies >= maxCandies);
    // Добавляем true или false в массив результатов
  }

  return result;
};

